import React, { useState } from "react"

const ShopCart = ({ shopItems, addToCart }) => {
  const [count, setCount] = useState(0)
  const increment = () => {
    setCount(count + 1)
  }

  return (
    <>
      {shopItems.map((shopItems, index) => {
        return (
          <div className='box'>
            <div className='product mtop cursor-pointer'>
              <div className='space-y-2'>
                <span className='bg-gradient-to-r from-purple-500 via-purple-600 to-purple-700 text-white text-sm px-2 py-1 rounded-full'>{shopItems.discount}% Off</span>
                <img className='h-[15rem] mx-auto' src={shopItems.cover} alt='' />
                <div className='product-like'>
                  <label>{count}</label> <br />
                  <i className='fa-regular fa-heart' onClick={increment}></i>
                </div>
              </div>
              <div className='product-details'>
                <h3>{shopItems.name}</h3>
                <div className='rate'>
                  <i className='fa fa-star'></i>
                  <i className='fa fa-star'></i>
                  <i className='fa fa-star'></i>
                  <i className='fa fa-star'></i>
                  <i className='fa fa-star'></i>
                </div>
                <div className='d_flex my-2 items-center'>
                  <h4 className="text-[#8f0fff]">Rs.{shopItems.price}</h4>
                  <button className="px-2 py-1 rounded-sm bg-gradient-to-r from-purple-500 via-purple-600 to-purple-700 hover:bg-gradient-to-br" onClick={() => addToCart(shopItems)}>
                    <i className='fa fa-plus text-white'></i>
                  </button>
                </div>
              </div>
            </div>
          </div>
        )
      })}
    </>
  )
}

export default ShopCart
